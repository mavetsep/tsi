a = float(input('Digite o valor de A: '))
b = float(input('Digite o valor de B: '))
c = a + b
print('A + B --> [{}]'.format(c))
c = a * b
print('A * B --> [{}]'.format(c))
c = a - b
print('A - B --> [{}]'.format(c))
c = a / b
print('A / B --> [{}]'.format(c))
print('\nA > B --> [{}]'.format(a>b))
print('A <= B --> [{}]'.format(a<=b))
print('A != B --> [{}]'.format(a!=b))
print('A == B --> [{}]'.format(a==b))